def _call_llm_api(prompt: str) -> str:
    """
    Function for making an API call to a Large Language Model.

    Args:
        prompt: The fully-formed prompt to send to the LLM.

    Returns:
        The HTML string generated by the LLM.
    """
    ai_msg = None
    # Use centralized config for models
    export_models = MODEL_CONFIG.export_models
    
    for model in export_models:
        try:
            print(f"--- Attempting to generate HTML with model: {model} ---")
            llm = ChatGoogleGenerativeAI(
                model=model,
                temperature=0,
                max_tokens=None,
                timeout=MODEL_CONFIG.llm_timeout,
                max_retries=MODEL_CONFIG.max_retries,
                google_api_key=get_api_key("export")
            )
            ai_msg = llm.invoke(prompt)
            print(f"--- RETURNING HTML RESPONSE FROM {model} ---")
            return ai_msg.content
        except Exception as e:
            print(f"Error with model {model}: {e}")
            continue
    
    print("All export models failed. Saving the prompt to 'output/partials/export_html_prompt.txt'.")
    os.makedirs("./output/partials", exist_ok=True)
    with open("./output/partials/export_html_prompt.txt", "w", encoding="utf-8") as f:
        f.write(prompt)
    return "Failed to generate HTML due to API errors. The prompt has been saved to 'output/partials/export_html_prompt.txt'."

