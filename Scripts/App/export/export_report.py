def smart_export(
    content: str,
    filename: str,
    preferred_format: str = "markdown",
    use_llm: bool = False
) -> str:
    """
    Smart export that prioritizes FREE options to save API credits.
    
    Export Priority:
    1. Markdown (FREE - no LLM needed)
    2. PDF/Word (uses LLM for HTML generation)
    
    Args:
        content: Report content to export
        filename: Output filename (without extension)
        preferred_format: 'markdown', 'pdf', or 'word'
        use_llm: Force use of LLM-based export
    
    Returns:
        Path to exported file
    """
    from Scripts.App.export.markdown_export import export_to_markdown, MarkdownExporter
    
    os.makedirs("./output", exist_ok=True)
    
    # FREE OPTION: Markdown export (no LLM needed)
    if preferred_format.lower() == "markdown" or not use_llm:
        print("📝 Using FREE markdown export (no API credits used)")
        
        exporter = MarkdownExporter(
            output_dir="./output",
            add_metadata=True,
            with_toc=True  # Add table of contents
        )
        result_path = exporter.export(content, filename)
        print(f"✅ Exported to: {result_path}")
        return str(result_path)
    
    # LLM-BASED OPTION: PDF or Word (uses API credits)
    if preferred_format.lower() in ["pdf", "word"]:
        print(f"📄 Using LLM-based {preferred_format.upper()} export (uses API credits)")
        
        exporter = ExportReport(
            raw_text=content,
            output_format=preferred_format.lower(),
            output_filename=filename,
            font=Font.ROBOTO
        )
        exporter.export()
        ext = "pdf" if preferred_format.lower() == "pdf" else "docx"
        return f"./output/{filename} by NeetResearch App.{ext}"
    
    # Fallback to markdown
    print("⚠️ Unknown format, defaulting to FREE markdown export")
    return str(export_to_markdown(content, filename))


def export_with_fallback(
    content: str,
    filename: str,
    formats: list = None
) -> str:
    """
    Try multiple export formats with automatic fallback.
    
    Tries formats in order, falling back to next if one fails.
    Default order prioritizes FREE options.
    
    Args:
        content: Content to export
        filename: Output filename
        formats: List of formats to try ['markdown', 'pdf', 'word']
    
    Returns:
        Path to exported file
    """
    if formats is None:
        # Default: prioritize FREE option
        formats = ["markdown", "pdf"]
    
    last_error = None
    
    for fmt in formats:
        try:
            print(f"🔄 Attempting {fmt} export...")
            
            if fmt == "markdown":
                return smart_export(content, filename, "markdown", use_llm=False)
            else:
                return smart_export(content, filename, fmt, use_llm=True)
                
        except Exception as e:
            print(f"⚠️ {fmt} export failed: {e}")
            last_error = e
            continue
    
    # All formats failed
    raise Exception(f"All export formats failed. Last error: {last_error}")


# Convenience function for quick free export
def free_export(content: str, filename: str = None) -> str:
    """
    Quick FREE markdown export with no API usage.
    
    Args:
        content: Report content
        filename: Optional filename
    
    Returns:
        Path to exported markdown file
    """
    return smart_export(content, filename or "research_report", "markdown", use_llm=False)
